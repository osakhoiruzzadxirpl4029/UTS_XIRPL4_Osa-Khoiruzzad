    private void bt1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt1ActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggalPinjam = dateFormat.format(d1.getDate());
        String tanggalKembali = dateFormat.format(d2.getDate());
        String jenis = (String)c1.getSelectedItem();
        if ("".equals(t1.getText()) || "".equals(t2.getText()) ||
            "".equals(t3.getText()) || "".equals(t4.getText())||tanggalPinjam.equals("")||tanggalKembali.equals("")||jenis.equals("")){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            String JK="";

            if(b1.isSelected())
            JK = "Kembali";
            else
            JK = "Belum";

            String SQL = "INSERT INTO t_peminjam (NoAnggota,NamaPeminjam,AlamatPeminjam,JudulBuku,Status,TanggalPinjam,TanggalKembali,Jenis) "
            + "VALUES('"+t1.getText()+"','"+t2.getText()+"',"
            + "'"+t3.getText()+"','"+t4.getText()+"','"+JK+"','"+tanggalPinjam+"','"+tanggalKembali+"','"+jenis+"')";
            int status = KoneksiDB.execute(SQL);
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else
            JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bt1ActionPerformed

    private void tb1DataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb1DataMouseClicked
        int baris = tb1Data.getSelectedRow();
        if(baris != -1) {
            t1.setText(tb1Data.getValueAt(baris,0).toString());
            t2.setText(tb1Data.getValueAt(baris,1).toString());
            
            t3.setText(tb1Data.getValueAt(baris,2).toString());
            t4.setText(tb1Data.getValueAt(baris,3).toString());
            if("Kembali".equals(tb1Data.getValueAt(baris,4).toString())) {
                b1.setSelected(true);
            } else {
                b2.setSelected(true);
            }
                        
        }
    }//GEN-LAST:event_tb1DataMouseClicked

