    private void selectData() {
        String kolom[]={"NoAnggota","NamaPeminjam","AlamatPeminjam","JudulBuku","Status","TanggalPinjam","TanggalKembali","Jenis"};
        DefaultTableModel dtm = new DefaultTableModel(null,kolom);
        String SQL= "SELECT * FROM t_peminjam";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String NoAnggota = rs.getString(1);
                String NamaPeminjam = rs.getString(2);
                String Status = "";
                if("Kembali".equals(rs.getString(5))){
                    Status = "Kembali";
                }
                else{
                    Status = "Belum";
                }
                String AlamatPeminjam = rs.getString(3);
                String JudulBuku = rs.getString(4);
                String TanggalPinjam = rs.getString(6);
                String TanggalKembali = rs.getString(7);
                String Jenis = rs.getString(8);
               
                String data[]={NoAnggota,NamaPeminjam,AlamatPeminjam,JudulBuku,Status,TanggalPinjam,TanggalKembali,Jenis};
                dtm.addRow(data);
            }}
        catch (SQLException ex){
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE,null,ex);
                }
        tb1Data.setModel(dtm);
    }
